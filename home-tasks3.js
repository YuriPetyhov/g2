//Задача 2
//Префиксный вариант ++i:
let i = 0;
while (++i < 5) alert( i );
/* префиксный вариант увеличивает переменную на 1 и возвращает новое значение
соответственно, цикл последовательно выведет 1, 2, 3, 4
(в первой итерации начальное значение i сразу увеличится до 1, далее проверится неравенство (1 < 5),
после чего выведется i = 1;
аналогично, вторая итерация - i = 2, третья - i = 3, четвертся - i = 4, и на пятой i = 5 приведет к false */

//Постфиксный вариант i++
let i = 0;
while (i++ < 5) alert( i );
/* постфиксный вариант, наоборот, вначале возвращает старое значение переменной,
а затем увеличивает переменную на 1;
соответственно, цикл последовательно выведет 1, 2, 3, 4, 5
(на первой итерации сразу проверится неравенство (0 < 5), после чего i = 0 увеличится на 1 и выведется 1;
соответственно, вторая итерация - (1 < 5), i = 1 + 1, выводится 2;
третья - (2 < 5), i = 2 + 1, выводится 3;
четвертая - (3 < 5), i = 3 + 1, выводится 4;
пятая - (4 < 5), i = 4 + 1, выводится 5 */


